//while example
var numOfCustomers = 10;

while (numOfCustomers > 0) {
  console.log( "How may I help you?" );

  // help the customer ...

  numOfCustomers = numOfCustomers - 1;
}



// do..while example
numOfCustomers = 10

do {
  console.log( "How May I help you?" );

  //help the customer

  numOfCustomers = numOfCustomers - 1;
} while (numOfCustomers > 0 );
